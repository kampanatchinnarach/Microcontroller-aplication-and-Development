void displayHEX(uint32_t decimalNum) {
	char hexString[20];
	sprintf(hexString, "ADC1_CH10 0x%08X", decimalNum);
	while (__HAL_UART_GET_FLAG(&huart3, UART_FLAG_TC) == RESET) {
	}
	HAL_UART_Transmit(&huart3, (uint8_t*) hexString, strlen(hexString), 1000);
	HAL_Delay(400);
}

void mapRtoV(uint32_t decimalNum) {
	float voltVal = (3.3 / 4095) * decimalNum;
	char voltValStr[30];

	sprintf(voltValStr, " Vin = %.2f V\r\n", voltVal);
	while (__HAL_UART_GET_FLAG(&huart3, UART_FLAG_TC) == RESET) {
	}
	HAL_UART_Transmit(&huart3, (uint8_t*) voltValStr, strlen(voltValStr), 1000);
	HAL_Delay(400);

	if(voltVal > 0.66 && voltVal <= 1.32){
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, 1);
		HAL_Delay(100);
	}else if(voltVal < 0.66 ){
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, 0);
		HAL_Delay(100);
	}

	if(voltVal > 1.32 && voltVal <= 1.98){
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, 1);
		HAL_Delay(100);
	}else if(voltVal < 1.32 ){
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, 0);
		HAL_Delay(100);
	}

	if(voltVal > 1.98 && voltVal <= 2.64){
		HAL_GPIO_WritePin(GPIOE, GPIO_PIN_15, 1);
		HAL_Delay(100);
	}else if(voltVal < 1.98 ){
		HAL_GPIO_WritePin(GPIOE, GPIO_PIN_15, 0);
		HAL_Delay(100);
	}

	if(voltVal > 2.64 && voltVal <= 3.30){
		HAL_GPIO_WritePin(GPIOE, GPIO_PIN_14, 1);
		HAL_Delay(100);
	}else if(voltVal < 2.64 ){
		HAL_GPIO_WritePin(GPIOE, GPIO_PIN_14, 0);
		HAL_Delay(100);
	}

}

int average_8(int x){
	static int samples[8];
	static int i = 0;
	static int total = 0;

	/* Update the moving average */
	total += x - samples[i];
	samples[i] = x;

	/* Update the index */
	i = (i==7 ? 0 : i+1);

	return total >>3;
}

int average_16(int x){
	static int samples[16];
	static int i = 0;
	static int total = 0;

	/* Update the moving average */
	total += x - samples[i];
	samples[i] = x;

	/* Update the index */
	i = (i==15 ? 0 : i+1);

	return total>>4;
}
